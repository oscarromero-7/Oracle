COL "NOMBRE AREA" FORMAT A20
COL "CATEGORIA" FORMAT A15
COL "APELLIDO Y NOMBRE" FORMAT A25
COL "SALARIO" FORMAT 999,999,999
SELECT A.NOMBRE_AREA AS "NOMBRE AREA",
	E.APELLIDO || ' ' || E.NOMBRE AS "APELLIDO Y NOMBRE",
	E.FECHA_ING AS "FECHA INGRESO",
	CS.NOMBRE_CAT AS "CATEGORIA",
	P.SALARIO_BASICO AS "SALARIO"
FROM B_POSICION_ACTUAL PA
INNER JOIN B_EMPLEADOS E ON E.CEDULA = PA.CEDULA
INNER JOIN B_CATEGORIAS_SALARIALES CS ON CS.COD_CATEGORIA = PA.COD_CATEGORIA
INNER JOIN B_AREAS A ON A.ID = PA.ID_AREA
INNER JOIN B_PLANILLA P ON P.CEDULA = E.CEDULA 
WHERE PA.FECHA_FIN IS NULL
ORDER BY A.NOMBRE_AREA,E.APELLIDO
/
