COL "DIRECCION" FORMAT A20
SET LINESIZE 100
SELECT TO_NUMBER(P.CEDULA) AS "CEDULA",
	P.APELLIDO AS "APELLIDO",
	P.NOMBRE AS "NOMBRE",
	P.DIRECCION AS "DIRECCION",
	P.TELEFONO AS "TELEFONO",
	P.FECHA_NACIMIENTO AS "FECHA NACIMIENTO"
FROM B_PERSONAS P
WHERE P.TIPO_PERSONA='F' AND P.CEDULA IS NOT NULL

UNION
SELECT E.CEDULA AS "CEDULA",
	E.APELLIDO AS "APELLIDO",
	E.NOMBRE AS "NOMBRE",
	E.DIRECCION AS "DIRECCION",
	E.TELEFONO AS "TELEFONO",
	E.FECHA_NACIM AS "FECHA NACIMIENTO"
FROM B_EMPLEADOS E

/
