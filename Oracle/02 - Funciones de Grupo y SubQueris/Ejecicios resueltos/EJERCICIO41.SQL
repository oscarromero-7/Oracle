SELECT
	TO_CHAR(MAX(AUX.SALARIO),'999,999,999') AS "SALARIO MAXIMO",
	TO_CHAR(AVG(AUX.SALARIO),'999,999,999') AS "SALARIO PROMEDIO",
	TO_CHAR(MIN(AUX.SALARIO),'999,999,999') AS "SALARIO MINIMO"
FROM (
	SELECT
		CS.ASIGNACION AS SALARIO
	FROM B_EMPLEADOS E
	INNER JOIN B_POSICION_ACTUAL PA ON PA.CEDULA = E.CEDULA
	INNER JOIN B_CATEGORIAS_SALARIALES CS ON PA.COD_CATEGORIA = CS.COD_CATEGORIA
	WHERE CS.FECHA_FIN IS NULL AND PA.FECHA_FIN IS NULL
) AUX

