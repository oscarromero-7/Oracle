SELECT DECODE(P.TIPO_PERSONA,'F',P.CEDULA,'J',P.RUC,'SIN DOCUMENTOS') AS IDENTIFICACION,
	P.NOMBRE,
	P.APELLIDO,
	L.NOMBRE AS LOCALIDAD
FROM B_PERSONAS P
INNER JOIN B_LOCALIDAD L ON P.ID_LOCALIDAD = L.ID
WHERE P.ID_LOCALIDAD = (SELECT PP.ID_LOCALIDAD
			FROM B_PERSONAS PP
			WHERE UPPER(PP.NOMBRE) LIKE 'SERVIMEX')
/
