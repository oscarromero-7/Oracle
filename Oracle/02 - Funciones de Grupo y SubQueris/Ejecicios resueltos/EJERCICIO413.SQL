SELECT TO_CHAR(E.CEDULA) AS CEDULA,
	E.NOMBRE ||' '||E.APELLIDO AS "APELLIDO Y NOMBRE",
	PA.COD_CATEGORIA,
	CS.NOMBRE_CAT AS "NOMBRE CATEGORIA",
	TO_CHAR(CS.ASIGNACION,'999,999,999') AS "ASIGNACION"
FROM B_EMPLEADOS E
INNER JOIN B_POSICION_ACTUAL PA ON E.CEDULA = PA.CEDULA AND PA.FECHA_FIN IS NULL
INNER JOIN B_CATEGORIAS_SALARIALES CS ON PA.COD_CATEGORIA = CS.COD_CATEGORIA AND CS.FECHA_FIN IS NULL
WHERE CS.ASIGNACION < 2041123
/
