COL "NOMBRE AREA" FORMAT A30
COL "NOMBRE Y APELLIDO" FORMAT A40
SELECT A.ID AS "ID AREA",
	A.NOMBRE_AREA AS "NOMBRE AREA",
	E.CEDULA AS "CEDULA",
	E.NOMBRE||' '||E.APELLIDO AS "NOMBRE Y APELLIDO",
	E.FECHA_ING AS "FECHA INGRESO"
FROM B_EMPLEADOS E
INNER JOIN B_POSICION_ACTUAL PA ON E.CEDULA = PA.CEDULA AND PA.FECHA_FIN IS NULL
INNER JOIN B_AREAS A ON PA.ID_AREA = A.ID AND UPPER(A.ACTIVA) = 'S'
ORDER BY A.ID ASC,E.FECHA_ING DESC
/
