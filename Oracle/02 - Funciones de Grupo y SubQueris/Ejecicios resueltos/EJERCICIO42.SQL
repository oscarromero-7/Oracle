SELECT
	AUX.AREA AS "NOMBRE AREA",
	TO_CHAR(MAX(AUX.SALARIO),'999,999,999') AS "ASIGNACION MAXIMA",
	TO_CHAR(MIN(AUX.SALARIO),'999,999,999') AS "ASIGNACION MINIMA"
FROM (
	SELECT
		CS.ASIGNACION AS SALARIO,
		A.NOMBRE_AREA AS AREA
	FROM B_EMPLEADOS E
	INNER JOIN B_POSICION_ACTUAL PA ON PA.CEDULA = E.CEDULA
	INNER JOIN B_CATEGORIAS_SALARIALES CS ON PA.COD_CATEGORIA = CS.COD_CATEGORIA
	INNER JOIN B_AREAS A ON PA.ID_AREA = A.ID
	WHERE CS.FECHA_FIN IS NULL AND PA.FECHA_FIN IS NULL
) AUX
GROUP BY AUX.AREA
/
