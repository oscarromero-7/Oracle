INSERT INTO B_POSICION_ACTUAL (ID, COD_CATEGORIA, CEDULA, ID_AREA, FECHA_INI)
VALUES (
  (SELECT MAX(ID) + 1 FROM B_POSICION_ACTUAL),
  (SELECT DISTINCT PA.COD_CATEGORIA
   FROM B_POSICION_ACTUAL PA
   INNER JOIN B_EMPLEADOS E ON E.CEDULA = PA.CEDULA
   WHERE UPPER(E.NOMBRE) LIKE 'AMANDA' AND UPPER(E.APELLIDO) LIKE 'P_REZ%'),
  (SELECT DISTINCT E.CEDULA
   FROM B_POSICION_ACTUAL PA
   INNER JOIN B_EMPLEADOS E ON E.CEDULA = PA.CEDULA
   WHERE UPPER(E.NOMBRE) LIKE 'RICARDO' AND UPPER(E.APELLIDO) LIKE 'MEZA'),
  (SELECT DISTINCT PA.ID_AREA
   FROM B_POSICION_ACTUAL PA
   INNER JOIN B_EMPLEADOS E ON E.CEDULA = PA.CEDULA
   WHERE UPPER(E.NOMBRE) LIKE 'AMANDA' AND UPPER(E.APELLIDO) LIKE 'P_REZ%'),
  SYSDATE
)

