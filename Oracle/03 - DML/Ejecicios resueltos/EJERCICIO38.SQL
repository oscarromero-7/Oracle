INSERT FIRST
WHEN V.TIPO_VENTA = 'CO' THEN INTO VENTAS_CONTADO2011
WHEN V.TIPO_VENTA = 'CR' THEN INTO VENTAS_CREDITO2011
SELECT DV.ID_ARTICULO,
	EXTRACT(MONTH FROM V.FECHA) MES,
	SUM(DV.CANTIDAD) CANTIDAD,
	SUM(DV.CANTIDAD * DV.PRECIO) TOTAL_VENTA
FROM B_VENTAS V
INNER JOIN B_DETALLE_VENTAS DV ON V.ID = DV.ID_VENTA
WHERE EXTRACT(YEAR FROM V.FECHA) = 2018
GROUP BY DV.ID_ARTICULO, EXTRACT(MONTH FROM V.FECHA)
