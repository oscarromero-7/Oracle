COL TABLE_NAME FORMAT A30
COL COLUMN_NAME FORMAT A25
COL DATA_TYPE FORMAT A11
COL TIPO FORMAT A15
COL COMMENTS FORMAT A50
CREATE OR REPLACE VIEW V_DESCRIPCION AS
SELECT UC.TABLE_NAME,
	UC.COLUMN_NAME,
	DECODE(UC.NULLABLE,'N','NOT NULL','') AS "Nulo?",
	UC.DATA_TYPE||'('||UC.DATA_LENGTH||')' AS TIPO,
	CC.COMMENTS
FROM USER_TAB_COLUMNS UC
INNER JOIN USER_COL_COMMENTS CC ON UC.TABLE_NAME = CC.TABLE_NAME AND CC.COLUMN_NAME = UC.COLUMN_NAME
