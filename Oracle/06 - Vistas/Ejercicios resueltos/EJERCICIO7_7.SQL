CREATE MATERIALIZED VIEW V_CLIENTESXREGION 
	BUILD IMMEDIATE
	REFRESH START WITH TRUNC(SYSDATE) - 1/24
	NEXT NEXT_DAY(TRUNC(SYSDATE), 7) 
	AS
	SELECT COUNT(DECODE(P.ID_LOCALIDAD, 13, 1, NULL)) AS ASUNCION,
		COUNT(DECODE(P.ID_LOCALIDAD, 21, 1, NULL)) AS "SAN LORENZO",
		COUNT(DECODE(P.ID_LOCALIDAD, 10, 1, NULL)) AS "FERNANDO DE LA MORA",
		COUNT(DECODE(P.ID_LOCALIDAD, 14, 1, NULL)) AS LAMBARE
	FROM B_LOCALIDAD L
	INNER JOIN B_PERSONAS P ON L.ID = P.ID_LOCALIDAD
	WHERE UPPER(P.ES_CLIENTE) = 'S'
/